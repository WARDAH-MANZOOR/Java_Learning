package com.company.chapter5;
// LOOPS: Sometimes we want our programs to execute a few sets of instructions over and over again for this purpose we use loops
// TYPES OF LOOPS: There are 3 types of loops: 1-- While Loop, 2-- Do while Loop , 3-- For Loop
public class WhileLoop {
    public static void main(String[] args) {
        int i = 1;
        while(i <= 10){
            System.out.println(i);
            i++;
        }
        System.out.println("Finish Running While Loop!");

    }
}
